
""" Heterogeneous Data Types Example """

# List with mixed data types
mixed_list = ["Python", 26, [1, 2, 3]]

# Tuple with different data types
mixed_tuple = ("Dragon", True, 42, 5.6)

# Set with mixed immutable types
mixed_set = {11, "PyCodeArena", 3.14}

# Dictionary with mixed key-value pairs
mixed_dict = {1: "One", 3.14: [1, 2]}

